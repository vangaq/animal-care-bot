def make_response_ok(data: Dict[str, Any]) -> Dict[str, Any]:
    """Возвращает успешный ответ в едином формате."""
    return {"status": "ok", "data": data}


def make_response_error(error_msg: str) -> Dict[str, Any]:
    """Возвращает ответ с ошибкой в едином формате."""
    return {"status": "error", "data": {}, "error_msg": error_msg}


def now_iso() -> str:
    """Возвращает текущее UTC-время в ISO-формате."""
    return datetime.utcnow().isoformat()


def get_species_detail_label(species: str | None) -> str:
    """Возвращает понятное название поля для конкретного вида питомца."""
    return SPECIES_DETAIL_LABELS.get((species or "").strip(), "Порода")


def get_species_detail_prompt(species: str | None) -> str:
    """Возвращает персонализированную подсказку для ввода вида/породы."""
    return SPECIES_DETAIL_PROMPTS.get((species or "").strip(), "Введите породу питомца:")


def get_species_detail_empty_text(species: str | None) -> str:
    """Возвращает текст ошибки для пустого ввода вида/породы."""
    return SPECIES_DETAIL_EMPTY_TEXTS.get((species or "").strip(), "Порода не может быть пустой. Введите ещё раз:")
